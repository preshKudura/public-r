                          <p class="lead">PROFESSIONAL ADMISSION REQUIREMENTS</p>  

                        To qualify for admission, a candidate must possess any of the following qualifications:
                        <ul class="link-list">
                        <li> A recognized University degree or Diploma in social sciences, arts and humanities or any other relevant discipline.</li> 
                       <li> Professional Certificate / Diploma from institutions recognized by the Governing Council of the Nigerian Institute of Public Relations for this purpose.</li> 
                       <li> BEEC Foundation Examination in Public Relations with passes in five subjects.</li> 
                       <li> GCE "A" Level in at least two subjects provided credit in English has been obtained at "O" Level or WASC, or SSS III Examination.</li> 
                        <li> NCE/Grade II Teachers' Certificate with at least five merits including English Language or its equivalent. (For this purpose, English Literature or General Paper is not a substitute for English Language)</li> 
                       <li> ND or HND</li> 
                        <li> Mature students with at least seven years of Public Relations experience.</li> 
                        <li> London Chambers of Commerce Higher Stage passes in two other subjects, which may be Public Relations.</li> 
                        <li>  Candidates should have five credits at ‘O’ levels including English Language and literature-in-English.</li>  
                       <li> Professional Certificate in Public Relations (CPR) of the Nigerian Institute of Public Relations or its equivalent as recognized by the NIPR Council</li> 
                       <li> A University degree in social sciences, arts and humanities or any other discipline or its equivalent recognized by the Institute.</li> 
                       <li> Holders of HND in Mass Communication, Communication Arts, Marketing or London CAM Certificate in Communication Studies</li> 
                        </ul>
                       <i>  NOTE: In special circumstances, consideration may be given to those with professional  experience in public relations who may not meet some of the above requirements.</i>
                    <br><br> 
                       <b> EXEMPTION </b> <br>

                        Those who meet the conditions for exemption as stipulated in the NIPR Law would be exempted from stages one and two of the exams and made to write only stage three (the last stage) of the exams in line with best practices among other notable professional bodies.
