		<p style="text-align: justify;">Governing Council
			At the helm of the Institute’s affairs is a twenty-two (22) member Council comprising 17
			registered public relations professionals elected every two years by members of the Institute
			 and five (5) members of the Institute nominated by the Federal Government. The Council is
			 chaired by the President who is assisted by a Vice President. The President and Vice
			  President are elected every two years through an electoral college made up of only
			  elected Council Members.
			<br>
			To ease the day-to-day administration of the Institute, the Law provides for the appointment
			of a Registrar by the Council. The Registrar is appointed for a fixed
			renewable term of five years and has the primary duty to prepare and maintain the register of
			 members by the rules made by the Council. The Registrar is assisted by a host of competent
			 and professional diverse workforce located at the Abuja National Secretariat and the Lagos
			 liaison office of the secretariat. Ultimately all acts undertaken by the NIPR’s officers and
			 staff are in the name of, and with the authority of, the Council.
			</p>
